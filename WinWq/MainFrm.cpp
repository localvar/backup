BOOL CMainFrame::OnCreateClient(LPCREATESTRUCT /*lpcs*/,
	CCreateContext* pContext)
{
	CRect r;
	GetClientRect(&r);
	
	m_wndSplitter.CreateStatic(this,1,2);
	m_wndSplitter.CreateView(0,0,RUNTIME_CLASS(CBoardView),CSize(r.bottom-r.top,0),pContext);
	m_wndSplitter.CreateView(0,1,RUNTIME_CLASS(CInfView),CSize(0,0),pContext);

	return true;
}

BOOL CMainFrame::PreCreateWindow(CREATESTRUCT& cs)
{

	if( !CFrameWnd::PreCreateWindow(cs) )
		return FALSE;
	// TODO: Modify the Window class or styles here by modifying
	//  the CREATESTRUCT cs
	cs.style = WS_OVERLAPPED | WS_CAPTION | FWS_ADDTOTITLE
		| WS_SYSMENU | WS_MINIMIZEBOX;
	cs.x=0;
	cs.y=0;
	cs.cx=::GetSystemMetrics(SM_CXFULLSCREEN);
	cs.cy=::GetSystemMetrics(SM_CYFULLSCREEN)+20;
	return TRUE;
}

